package ru.learning.task_mgt_system.model;

/**
 * Enumeration for defining the status of a task.
 */
public enum Status {
    /**
     * The task is pending.
     */
    PENDING,

    /**
     * The task is in progress.
     */
    IN_PROGRESS,

    /**
     * The task is completed.
     */
    COMPLETED
}